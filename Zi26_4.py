# Функция принимает кортеж кортежей и число k.
# Возвращает список, в котором в каждом вложенном кортеже последний элемент заменён на k.
def modify_lastelement(tp_tuple, k):
    """
    tp_tuple - кортеж, содержащий вложенные кортежи (например, ((1,2), (3,4,5))).
    k - новое значение, которое ставится в конец каждого вложенного кортежа.

    Функция создаёт новый список, где каждый внутренний кортеж преобразован:
    - все элементы, кроме последнего, остаются без изменений,
    - последний элемент заменяется на k.
    """
    # Создаём пустой список, который будем заполнять новыми кортежами.
    result = []

    # Перебираем все вложенные кортежи из переданного кортежа tp_tuple.
    for inner_tuple in tp_tuple:
        # Берём все элементы внутреннего кортежа, кроме последнего,
        # с помощью среза inner_tuple[:-1].
        # Затем добавляем к ним новое значение k в виде одноэлементного кортежа (k,).
        # Склеиваем две части через оператор + и получаем новый кортеж.
        new_inner = inner_tuple[:-1] + (k,)

        # Добавляем полученный изменённый кортеж в итоговый список.
        result.append(new_inner)

    # Возвращаем список изменённых кортежей.
    return result


# --- Проверка работы функции на заданных примерах ---

# Первый вложенный кортеж (1, 1, 2)
mytuple1 = (1, 1, 2)
# Второй вложенный кортеж (2, 4)
mytuple2 = (2, 4)

# Составляем кортеж из этих двух кортежей
tpl_tuple = (mytuple1, mytuple2)

# Вызываем функцию, заменяя последний элемент каждого внутреннего кортежа на 5
mylist2 = modify_lastelement(tpl_tuple, 5)

# Печатаем результат
print(mylist2)  # Ожидаемый вывод: [(1, 1, 5), (2, 5)]

# Для наглядности проверим, что исходные кортежи не изменились
print("mytuple1:", mytuple1)  # (1, 1, 2) — не изменился
print("mytuple2:", mytuple2)  # (2, 4) — не изменился
print("tpl_tuple:", tpl_tuple)  # ((1, 1, 2), (2, 4)) — не изменился