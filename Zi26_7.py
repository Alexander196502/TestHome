# Программа для преобразования введенного текста в заглавные буквы с восклицательным знаком

# Запрашиваем у пользователя ввод текста с клавиатуры
# Функция input() считывает строку, введенную пользователем
user_text = input("Введите некоторый текст: ")

# Преобразуем весь текст в заглавные буквы
# Метод upper() возвращает копию строки, где все символы переведены в верхний регистр
uppercase_text = user_text.upper()

# Добавляем восклицательный знак в конце строки
# Оператор + выполняет конкатенацию (склеивание) строк
final_text = uppercase_text + "!"

# Выводим результат на экран
# Функция print() отображает переданное ей значение в консоли
print(final_text)

